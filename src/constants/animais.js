export const getAnimalPorNumero = (numero) => {
  // Normaliza o número para string com 2 dígitos
  const numeroStr = numero.toString().padStart(2, '0');
  
  return animais.find(animal => 
    animal.numeros.includes(numeroStr)
  );
};

export const getAnimalPorId = (id) => {
  return animais.find(animal => animal.id === id);
};

export const getAnimalPorNome = (nome) => {
  return animais.find(animal => 
    animal.nomeChave.toLowerCase() === nome.toLowerCase() ||
    animal.nome.toLowerCase() === nome.toLowerCase()
  );
};